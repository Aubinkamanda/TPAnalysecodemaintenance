package ex1;
/***
 * un programme qui fait une addition et une soustraction de deux variables
 * @author Aubin KAMANDA
 */
public class Calcul {
    /***
     * fonction qui retourne la somme de deux variables
     * @param a
     * @param b
     * @return
     */
    public int getsum(int a, int b) {
        return a + b;
    }

    /***
     * fonction qui retourne la soustraction de deux variables
     * @param a
     * @param b
     * @return
     */
    public int getsubstraction(int a, int b) {
        return a - b;
    }
}
